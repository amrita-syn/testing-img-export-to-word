export const uncompressFile = file => {
  // console.log('uncompressing');
  return new Promise((resolve, reject) => {
    zlib.inflate(Buffer.from(file.body.data), (err, uncompressed) => {
      if (err) {
        reject(err);
      }
      let name = file.name;
      resolve({ uncompressed, name });
    });
  });
};
